package com.cg.loops;

public class Prog6 {
	public static void main(String[] args) {
		//i is initialized to 0, and should be less than 5
		//increment operator increases the value by 1 after each iteration
		for(int i = 0; i < 5; i++)
		{
		//prints hello 3 times, in 3 separate lines
		System.out.println("Hello");
		//increment operator
		//again increases value of i by 1
		i++;
	}
}
}
