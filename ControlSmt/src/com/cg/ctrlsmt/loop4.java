package com.cg.ctrlsmt;

public class loop4 {
	public static void main(String... args) {
		//i takes value from 1 to 3
		for(int i=1; i<=3; i++) {
			//j also takes values from 1 to 3
			for(int j=1; j<=3; j++) {
				//this will print i and j values in a single line
				//when the loop runs
				System.out.println(i+" "+j);
	}
	}
	}
}
