package com.cg.variables;

public class Prog8{
	public static void main(String... args) {
		//two types of type casting 
		//automatic and manual casting
		//automatic or widening casting takes place
		//when a data type of smaller size is assigned to larger size
		
		//declaring and assigning value to int variable
		int i1=10;
		//assigning the int variable to double variable
		//automatic type casting takes place
		double d1=i1;
		//prints the integer value stored in i1
		System.out.println(i1);
		//prints the double value of integer stored in i1
		System.out.println(d1);
	}
}
